    private void CargarDatos(){
        int ID;
        String Nombre;
        String Descripcion;
        String Categoria;
        String Tamano;
        Float Precio;
        boolean Disponibilidad;
        
        Scanner Lector;
        try {
            Lector = new Scanner(ArchivoMenu);
            while(Lector.hasNextLine()){
                String Partes[] = Lector.nextLine().split(" - ");
                ID = Integer.parseInt(Partes[0]);
                Nombre = Partes[1];
                Descripcion = Partes[2];
                Categoria = Partes[3];
                Tamano = Partes[4];
                Precio = Float.parseFloat(Partes[5]);
                Disponibilidad = Boolean.parseBoolean(Partes[6]);

                ModeloMenu.addRow(new Object[]{ID, Nombre, Descripcion, Categoria, Tamano, Precio, Disponibilidad});
            }
            Lector.close();
        } catch (FileNotFoundException ex) {
            System.getLogger(pnlConsultaMenu.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

    }

